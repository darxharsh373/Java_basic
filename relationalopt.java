import java.util.*;
public class relationalopt {
public static void main(String args[]){
 Scanner sc=new Scanner(System.in);
 System.out.println("Enter two number to relate:");
 int a=sc.nextInt();
 int b=sc.nextInt();
 System.out.println((a==b));
 System.out.println((a!=b));
 System.out.println((a>b));
 System.out.println((a<b));
 System.out.println((a>=b));
 System.out.println((a<=b));
}
}